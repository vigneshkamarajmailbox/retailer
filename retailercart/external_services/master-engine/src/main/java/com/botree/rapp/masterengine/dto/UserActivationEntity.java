package com.botree.rapp.masterengine.dto;

import java.util.Date;

/**
 * Application User Activation Entity class.
 * @author vinodkumar.a
 */
public class UserActivationEntity extends AbstractEntity {

    /** loginCode. */
    private String loginCode;

    /** userName. */
    private String userName;

    /** userStatus. */
    private String userStatus;

    /** creationDt. */
    private Date creationDt;

    /** msgRequestDt. */
    private Date msgRequestDt;

    /** activationDt. */
    private Date activationDt;

    /** deactivationDt. */
    private Date deactivationDt;

    /**
     * @return the loginCode
     */
    public final String getLoginCode() {
        return loginCode;
    }

    /**
     * @param loginCodeIn the loginCode to set
     */
    public final void setLoginCode(final String loginCodeIn) {
        loginCode = loginCodeIn;
    }

    /**
     * @return the userName
     */
    public final String getUserName() {
        return userName;
    }

    /**
     * @param userNameIn the userName to set
     */
    public final void setUserName(final String userNameIn) {
        userName = userNameIn;
    }

    /**
     * @return the userStatus
     */
    public final String getUserStatus() {
        return userStatus;
    }

    /**
     * @param userStatusIn the userStatus to set
     */
    public final void setUserStatus(final String userStatusIn) {
        userStatus = userStatusIn;
    }

    /**
     * @return the creationDt
     */
    public final Date getCreationDt() {
        return creationDt;
    }

    /**
     * @param creationDtIn the creationDt to set
     */
    public final void setCreationDt(final Date creationDtIn) {
        creationDt = creationDtIn;
    }

    /**
     * @return the msgRequestDt
     */
    public final Date getMsgRequestDt() {
        return msgRequestDt;
    }

    /**
     * @param msgRequestDtIn the msgRequestDt to set
     */
    public final void setMsgRequestDt(final Date msgRequestDtIn) {
        msgRequestDt = msgRequestDtIn;
    }

    /**
     * @return the activationDt
     */
    public final Date getActivationDt() {
        return activationDt;
    }

    /**
     * @param activationDtIn the activationDt to set
     */
    public final void setActivationDt(final Date activationDtIn) {
        activationDt = activationDtIn;
    }

    /**
     * @return the deactivationDt
     */
    public final Date getDeactivationDt() {
        return deactivationDt;
    }

    /**
     * @param deactivationDtIn the deactivationDt to set
     */
    public final void setDeactivationDt(final Date deactivationDtIn) {
        deactivationDt = deactivationDtIn;
    }
}
