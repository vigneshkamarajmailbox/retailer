package com.botree.rapp.masterengine.dto;

/**
 * Application User Entity class.
 * @author vinodkumar.a
 */
public class AppUserEntity extends AbstractEntity {

    /** loginCode. */
    private String loginCode;

    /** password. */
    private String password;

    /** userName. */
    private String userName;

    /** userStatus. */
    private String userStatus;

    /** newPassword. */
    private String newPassword;

    /** groupCode. */
    private String groupCode;

    /** loginStatus. */
    private String loginStatus;

    /** deviceId. */
    private String deviceId;

    /**
     * @return the loginCode
     */
    public final String getLoginCode() {
        return loginCode;
    }

    /**
     * @param loginCodeIn the loginCode to set
     */
    public final void setLoginCode(final String loginCodeIn) {
        loginCode = loginCodeIn;
    }

    /**
     * @return the password
     */
    public final String getPassword() {
        return password;
    }

    /**
     * @param passwordIn the password to set
     */
    public final void setPassword(final String passwordIn) {
        password = passwordIn;
    }

    /**
     * @return the userName
     */
    public final String getUserName() {
        return userName;
    }

    /**
     * @param userNameIn the userName to set
     */
    public final void setUserName(final String userNameIn) {
        userName = userNameIn;
    }

    /**
     * @return the userStatus
     */
    public final String getUserStatus() {
        return userStatus;
    }

    /**
     * @param userStatusIn the userStatus to set
     */
    public final void setUserStatus(final String userStatusIn) {
        userStatus = userStatusIn;
    }

    /**
     * @return the newPassword
     */
    public final String getNewPassword() {
        return newPassword;
    }

    /**
     * @param newPasswordIn the newPassword to set
     */
    public final void setNewPassword(final String newPasswordIn) {
        newPassword = newPasswordIn;
    }

    /**
     * @return the groupCode
     */
    public final String getGroupCode() {
        return groupCode;
    }

    /**
     * @param groupCodeIn the groupCode to set
     */
    public final void setGroupCode(final String groupCodeIn) {
        groupCode = groupCodeIn;
    }

    /**
     * @return the loginStatus
     */
    public final String getLoginStatus() {
        return loginStatus;
    }

    /**
     * @param loginStatusIn the loginStatus to set
     */
    public final void setLoginStatus(final String loginStatusIn) {
        this.loginStatus = loginStatusIn;
    }

    /**
     * @return the deviceId
     */
    public final String getDeviceId() {
        return deviceId;
    }

    /**
     * @param deviceIdIn the deviceId to set
     */
    public final void setDeviceId(final String deviceIdIn) {
        this.deviceId = deviceIdIn;
    }
}
