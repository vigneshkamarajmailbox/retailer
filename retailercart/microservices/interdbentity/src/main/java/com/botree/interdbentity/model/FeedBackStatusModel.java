package com.botree.interdbentity.model;

/**
 * Class to return the feedback and store visibility status.
 * @author vinodkumar.a
 */
public class FeedBackStatusModel {

    /** cmpCode. */
    private String cmpCode;

    /** distrCode. */
    private String distrCode;

    /** feedbackNo. */
    private String feedbackNo;

    /** uploadStatus. */
    private Boolean uploadStatus;

    /**
     * @return the cmpCode
     */
    public final String getCmpCode() {
        return cmpCode;
    }

    /**
     * @param cmpCodeIn the cmpCode to set
     */
    public final void setCmpCode(final String cmpCodeIn) {
        this.cmpCode = cmpCodeIn;
    }

    /**
     * @return the distrCode
     */
    public final String getDistrCode() {
        return distrCode;
    }

    /**
     * @param distrCodeIn the distrCode to set
     */
    public final void setDistrCode(final String distrCodeIn) {
        this.distrCode = distrCodeIn;
    }

    /**
     * @return the feedbackNo
     */
    public final String getFeedbackNo() {
        return feedbackNo;
    }

    /**
     * @param feedbackNoIn the feedbackNo to set
     */
    public final void setFeedbackNo(final String feedbackNoIn) {
        this.feedbackNo = feedbackNoIn;
    }

    /**
     * @return the uploadStatus
     */
    public final Boolean getUploadStatus() {
        return uploadStatus;
    }

    /**
     * @param uploadStatusIn the uploadStatus to set
     */
    public final void setUploadStatus(final Boolean uploadStatusIn) {
        this.uploadStatus = uploadStatusIn;
    }
}
