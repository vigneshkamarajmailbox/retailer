package com.botree.interdbentity.model;

import java.util.Date;

/**
 * Abstract Entity class.
 * @author vinodkumar.a
 */
public class AbstractEntity {

    /** cmpCode. */
    private String cmpCode;

    /** consoleFlag. */
    private String consoleFlag;

    /** uploadFlag. */
    private String uploadFlag;

    /** modDt. */
    private Date modDt;

    /** changeNo. */
    private Long changeNo;

    /**
     * @return the cmpCode
     */
    public final String getCmpCode() {
        return cmpCode;
    }

    /**
     * @param cmpCodeIn the cmpCode to set
     */
    public final void setCmpCode(final String cmpCodeIn) {
        cmpCode = cmpCodeIn;
    }

    /**
     * @return the consoleFlag
     */
    public final String getConsoleFlag() {
        return consoleFlag;
    }

    /**
     * @param consoleFlagIn the consoleFlag to set
     */
    public final void setConsoleFlag(final String consoleFlagIn) {
        consoleFlag = consoleFlagIn;
    }

    /**
     * @return the uploadFlag
     */
    public final String getUploadFlag() {
        return uploadFlag;
    }

    /**
     * @param uploadFlagIn the uploadFlag to set
     */
    public final void setUploadFlag(final String uploadFlagIn) {
        uploadFlag = uploadFlagIn;
    }

    /**
     * @return the modDt
     */
    public final Date getModDt() {
        return modDt;
    }

    /**
     * @param modDtIn the modDt to set
     */
    public final void setModDt(final Date modDtIn) {
        modDt = modDtIn;
    }

    /**
     * @return the changeNo
     */
    public final Long getChangeNo() {
        return changeNo;
    }

    /**
     * @param changeNoIn the changeNo to set
     */
    public final void setChangeNo(final Long changeNoIn) {
        changeNo = changeNoIn;
    }
}
