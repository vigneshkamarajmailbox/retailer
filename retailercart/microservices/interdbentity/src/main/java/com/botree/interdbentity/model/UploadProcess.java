package com.botree.interdbentity.model;

import java.util.List;

/**
 * This class contains the upload data from CSNG to InterDB.
 * @author vinodkumar.a
 */
public class UploadProcess {

    /** query. */
    private String query;

    /** distributors. */
    private List<String> distributors;

    /** jsonStr. */
    private String jsonStr;

    /** entity. */
    private String entity;

    /** jsonArr. */
    private byte[] jsonArr;

    /**
     * @return the query
     */
    public final String getQuery() {
        return query;
    }

    /**
     * @param queryIn the query to set
     */
    public final void setQuery(final String queryIn) {
        this.query = queryIn;
    }

    /**
     * @return the distributors
     */
    public final List<String> getDistributors() {
        return distributors;
    }

    /**
     * @param distributorsIn the distributors to set
     */
    public final void setDistributors(final List<String> distributorsIn) {
        this.distributors = distributorsIn;
    }

    /**
     * @return the jsonStr
     */
    public final String getJsonStr() {
        return jsonStr;
    }

    /**
     * @param jsonStrIn the jsonStr to set
     */
    public final void setJsonStr(final String jsonStrIn) {
        this.jsonStr = jsonStrIn;
    }

    /**
     * @return the entity
     */
    public final String getEntity() {
        return entity;
    }

    /**
     * @param entityIn the entity to set
     */
    public final void setEntity(final String entityIn) {
        this.entity = entityIn;
    }

    /**
     * @return the jsonArr
     */
    public final byte[] getJsonArr() {
        return jsonArr;
    }

    /**
     * @param jsonArrIn the jsonArr to set
     */
    public final void setJsonArr(final byte[] jsonArrIn) {
        jsonArr = jsonArrIn;
    }

}
