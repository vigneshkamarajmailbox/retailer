package com.botree.interdbentity.model;

/**
 * Class contains the application update related fields.
 * @author vinodkumar.a
 */
public class ApplicationUpdateEntity extends AbstractEntity {

    /** dbVersion. */
    private String dbVersion;

    /** appVersion. */
    private String appVersion;

    /** url. */
    private String url;

    /** fileName. */
    private String fileName;

    /** remarks. */
    private String remarks;

    /**
     * @return the dbVersion
     */
    public final String getDbVersion() {
        return dbVersion;
    }

    /**
     * @param dbVersionIn the dbVersion to set
     */
    public final void setDbVersion(final String dbVersionIn) {
        this.dbVersion = dbVersionIn;
    }

    /**
     * @return the appVersion
     */
    public final String getAppVersion() {
        return appVersion;
    }

    /**
     * @param appVersionIn the appVersion to set
     */
    public final void setAppVersion(final String appVersionIn) {
        this.appVersion = appVersionIn;
    }

    /**
     * @return the url
     */
    public final String getUrl() {
        return url;
    }

    /**
     * @param urlIn the url to set
     */
    public final void setUrl(final String urlIn) {
        this.url = urlIn;
    }

    /**
     * @return the fileName
     */
    public final String getFileName() {
        return fileName;
    }

    /**
     * @param fileNameIn the fileName to set
     */
    public final void setFileName(final String fileNameIn) {
        this.fileName = fileNameIn;
    }

    /**
     * @return the remarks
     */
    public final String getRemarks() {
        return remarks;
    }

    /**
     * @param remarksIn the remarks to set
     */
    public final void setRemarks(final String remarksIn) {
        this.remarks = remarksIn;
    }

}
