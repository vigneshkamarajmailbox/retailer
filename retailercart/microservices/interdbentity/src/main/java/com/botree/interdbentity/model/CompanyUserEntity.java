package com.botree.interdbentity.model;

/**
 * Company User Entity class.
 * @author vinodkumar.a
 */
public class CompanyUserEntity extends AbstractEntity {

    /** loginCode. */
    private String loginCode;

    /** userName. */
    private String userName;

    /** userType. */
    private String userType;

    /** mappedCode. */
    private String mappedCode;

    /** emailId. */
    private String emailId;

    /** mobileNo. */
    private String mobileNo;

    /** userStatus. */
    private String userStatus;

    /**
     * @return the loginCode
     */
    public final String getLoginCode() {
        return loginCode;
    }

    /**
     * @param loginCodeIn the loginCode to set
     */
    public final void setLoginCode(final String loginCodeIn) {
        loginCode = loginCodeIn;
    }

    /**
     * @return the userName
     */
    public final String getUserName() {
        return userName;
    }

    /**
     * @param userNameIn the userName to set
     */
    public final void setUserName(final String userNameIn) {
        userName = userNameIn;
    }

    /**
     * @return the userType
     */
    public final String getUserType() {
        return userType;
    }

    /**
     * @param userTypeIn the userType to set
     */
    public final void setUserType(final String userTypeIn) {
        userType = userTypeIn;
    }

    /**
     * @return the mappedCode
     */
    public final String getMappedCode() {
        return mappedCode;
    }

    /**
     * @param mappedCodeIn the mappedCode to set
     */
    public final void setMappedCode(final String mappedCodeIn) {
        mappedCode = mappedCodeIn;
    }

    /**
     * @return the emailId
     */
    public final String getEmailId() {
        return emailId;
    }

    /**
     * @param emailIdIn the emailId to set
     */
    public final void setEmailId(final String emailIdIn) {
        emailId = emailIdIn;
    }

    /**
     * @return the mobileNo
     */
    public final String getMobileNo() {
        return mobileNo;
    }

    /**
     * @param mobileNoIn the mobileNo to set
     */
    public final void setMobileNo(final String mobileNoIn) {
        mobileNo = mobileNoIn;
    }

    /**
     * @return the userStatus
     */
    public final String getUserStatus() {
        return userStatus;
    }

    /**
     * @param userStatusIn the userStatus to set
     */
    public final void setUserStatus(final String userStatusIn) {
        userStatus = userStatusIn;
    }
}
