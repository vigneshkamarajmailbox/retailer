package com.botree.interdbentity.model;

/**
 * Distributor Stock Entity class.
 * @author vinodkumar.a
 */
public class DistributorStockEntity extends AbstractEntity {

    /** distrCode. */
    private String distrCode;

    /** saleableStock. */
    private String saleableStock;

    /** prodCode. */
    private String prodCode;

    /** saleableQty. */
    private Integer saleableQty;

    /**
     * @return the distrCode
     */
    public final String getDistrCode() {
        return distrCode;
    }

    /**
     * @param distrCodeIn the distrCode to set
     */
    public final void setDistrCode(final String distrCodeIn) {
        distrCode = distrCodeIn;
    }

    /**
     * @return the saleableStock
     */
    public final String getSaleableStock() {
        return saleableStock;
    }

    /**
     * @param saleableStockIn the saleableStock to set
     */
    public final void setSaleableStock(final String saleableStockIn) {
        saleableStock = saleableStockIn;
    }

    /**
     * @return the prodCode
     */
    public final String getProdCode() {
        return prodCode;
    }

    /**
     * @param prodCodeIn the prodCode to set
     */
    public final void setProdCode(final String prodCodeIn) {
        prodCode = prodCodeIn;
    }

    /**
     * @return the saleableQty
     */
    public final Integer getSaleableQty() {
        return saleableQty;
    }

    /**
     * @param saleableQtyIn the saleableQty to set
     */
    public final void setSaleableQty(final Integer saleableQtyIn) {
        saleableQty = saleableQtyIn;
    }
}
