package com.botree.interdbentity.model;

/**
 * Class contains the fire base user fields.
 * @author vinodkumar.a
 */
    public class FireBaseUserEntity extends AbstractEntity {

    /** fireBaseKey. */
    private String fireBaseKey;

    /** loginCode. */
    private String loginCode;

    /**
     * @return the fireBaseKey
     */
    public final String getFireBaseKey() {
        return fireBaseKey;
    }

    /**
     * @param fireBaseKeyIn the fireBaseKey to set
     */
    public final void setFireBaseKey(final String fireBaseKeyIn) {
        fireBaseKey = fireBaseKeyIn;
    }

    /**
     * @return the loginCode
     */
    public final String getLoginCode() {
        return loginCode;
    }

    /**
     * @param loginCodeIn the loginCode to set
     */
    public final void setLoginCode(final String loginCodeIn) {
        loginCode = loginCodeIn;
    }
}
