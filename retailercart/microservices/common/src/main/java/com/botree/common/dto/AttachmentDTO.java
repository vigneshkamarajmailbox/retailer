package com.botree.common.dto;

import java.io.File;

/**
 * This class is used to hold Attachment data.
 * @author vinodkumar.a
 */
public class AttachmentDTO {

    /** name. */
    private String name;

    /** file. */
    private File file;

    /**
     * Empty constructor.
     */
    public AttachmentDTO() {
        //Empty constructor
    }

    /**
     * @param nameIn attachment name
     * @param fileIn attachment
     */
    public AttachmentDTO(final String nameIn, final File fileIn) {
        this.name = nameIn;
        this.file = fileIn;
    }

    /**
     * @return the name
     */
    public final String getName() {
        return name;
    }

    /**
     * @param attachmentNameIn the name to set
     */
    public final void setName(final String attachmentNameIn) {
        name = attachmentNameIn;
    }

    /**
     * @return the file
     */
    public final File getFile() {
        return file;
    }

    /**
     * @param attachmentIn the file to set
     */
    public final void setFile(final File attachmentIn) {
        file = attachmentIn;
    }
}
