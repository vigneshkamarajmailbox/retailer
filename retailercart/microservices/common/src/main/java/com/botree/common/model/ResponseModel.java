package com.botree.common.model;


public class ResponseModel {
    /** statusCode. */
    private int statusCode;

    /** message. */
    private String message;

    public ResponseModel(final int statusCodeIn, final String messageIn) {
        this.statusCode = statusCodeIn;
        this.message = messageIn;
    }

    /**
     * @return the statusCode
     */
    public final int getStatusCode() {
        return statusCode;
    }

    /**
     * @param statusCodeIn the statusCode to set
     */
    public final void setStatusCode(final int statusCodeIn) {
        statusCode = statusCodeIn;
    }

    /**
     * @return the message
     */
    public final String getMessage() {
        return message;
    }

    /**
     * @param messageIn the message to set
     */
    public final void setMessage(final String messageIn) {
        message = messageIn;
    }
}
