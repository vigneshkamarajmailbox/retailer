package com.botree.retailerssfa.models;

public class LoginModel {
    private String cmpCode;
    private String loginCode;
    private String password;
    private String distrCode;
    private String userName;
    private String userStatus;
    private String newPassword;
    private boolean loginStatus;
    private String message;
    private String token;
    private String syncDate;
    private String deviceBrand;
    private String deviceModel;
    private String deviceVersion;
    private String advId;

    @Override
    public String toString() {
        return "LoginModel{" +
                "cmpCode='" + cmpCode + '\'' +
                ", loginCode='" + loginCode + '\'' +
                ", password='" + password + '\'' +
                ", distrCode='" + distrCode + '\'' +
                ", userName='" + userName + '\'' +
                ", userStatus='" + userStatus + '\'' +
                ", newPassword='" + newPassword + '\'' +
                ", loginStatus=" + loginStatus +
                ", message='" + message + '\'' +
                ", token='" + token + '\'' +
                ", syncDate='" + syncDate + '\'' +
                ", deviceBrand='" + deviceBrand + '\'' +
                ", deviceModel='" + deviceModel + '\'' +
                ", deviceVersion='" + deviceVersion + '\'' +
                '}';
    }

    public LoginModel() {
    }

    public LoginModel(String cmpCode, String loginCode, String password, String distrCode,
                      String userName, String userStatus, String newPassword, boolean loginStatus) {
        this.cmpCode = cmpCode;
        this.loginCode = loginCode;
        this.password = password;
        this.distrCode = distrCode;
        this.userName = userName;
        this.userStatus = userStatus;
        this.newPassword = newPassword;
        this.loginStatus = loginStatus;
    }

    public LoginModel(String loginCode, String password,
                      String userName, String userStatus, String newPassword, boolean loginStatus) {
        this.loginCode = loginCode;
        this.password = password;
        this.userName = userName;
        this.userStatus = userStatus;
        this.newPassword = newPassword;
        this.loginStatus = loginStatus;
    }

    public String getAdvId() {
        return advId;
    }

    public void setAdvId(String advId) {
        this.advId = advId;
    }

    public String getDeviceBrand() {
        return deviceBrand;
    }

    public void setDeviceBrand(String deviceBrand) {
        this.deviceBrand = deviceBrand;
    }

    public String getDeviceModel() {
        return deviceModel;
    }

    public void setDeviceModel(String deviceModel) {
        this.deviceModel = deviceModel;
    }

    public String getDeviceVersion() {
        return deviceVersion;
    }

    public void setDeviceVersion(String deviceVersion) {
        this.deviceVersion = deviceVersion;
    }

    public boolean isLoginStatus() {
        return loginStatus;
    }

    public void setLoginStatus(boolean loginStatus) {
        this.loginStatus = loginStatus;
    }

    public String getCmpCode() {
        return cmpCode;
    }

    public void setCmpCode(String cmpCode) {
        this.cmpCode = cmpCode;
    }

    public String getLoginCode() {
        return loginCode;
    }

    public void setLoginCode(String loginCode) {
        this.loginCode = loginCode;
    }

    public String getDistrCode() {
        return distrCode;
    }

    public void setDistrCode(String distrCode) {
        this.distrCode = distrCode;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserStatus() {
        return userStatus;
    }

    public void setUserStatus(String userStatus) {
        this.userStatus = userStatus;
    }

    public String getNewPassword() {
        return newPassword;
    }

    public void setNewPassword(String newPassword) {
        this.newPassword = newPassword;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getSyncDate() {
        return syncDate;
    }

    public void setSyncDate(String syncDate) {
        this.syncDate = syncDate;
    }
}
