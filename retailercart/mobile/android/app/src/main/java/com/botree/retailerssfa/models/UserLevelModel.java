package com.botree.retailerssfa.models;

import com.google.gson.annotations.SerializedName;

import static com.botree.retailerssfa.service.JSONConstants.TAG_CURRENT_LEVEL_USER_CODE;
import static com.botree.retailerssfa.service.JSONConstants.TAG_CURRENT_LEVEL_USER_NAME;
import static com.botree.retailerssfa.service.JSONConstants.TAG_NEXT_LEVEL_USER_CODE;
import static com.botree.retailerssfa.service.JSONConstants.TAG_NEXT_LEVEL_USER_NAME;

public class UserLevelModel {


    @SerializedName(TAG_CURRENT_LEVEL_USER_CODE)
    private String currentLevelUserCode;

    @SerializedName(TAG_CURRENT_LEVEL_USER_NAME)
    private String currentLevelUserName;

    @SerializedName(TAG_NEXT_LEVEL_USER_CODE)
    private String nextLevelUserCode;

    @SerializedName(TAG_NEXT_LEVEL_USER_NAME)
    private String nextLevelUserName;

    public String getCurrentLevelUserCode() {
        return currentLevelUserCode;
    }

    public void setCurrentLevelUserCode(String currentLevelUserCode) {
        this.currentLevelUserCode = currentLevelUserCode;
    }

    public String getCurrentLevelUserName() {
        return currentLevelUserName;
    }

    public void setCurrentLevelUserName(String currentLevelUserName) {
        this.currentLevelUserName = currentLevelUserName;
    }

    public String getNextLevelUserCode() {
        return nextLevelUserCode;
    }

    public void setNextLevelUserCode(String nextLevelUserCode) {
        this.nextLevelUserCode = nextLevelUserCode;
    }

    public String getNextLevelUserName() {
        return nextLevelUserName;
    }

    public void setNextLevelUserName(String nextLevelUserName) {
        this.nextLevelUserName = nextLevelUserName;
    }
}
