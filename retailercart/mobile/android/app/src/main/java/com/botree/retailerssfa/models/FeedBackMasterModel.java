package com.botree.retailerssfa.models;

import com.google.gson.annotations.SerializedName;

import static com.botree.retailerssfa.service.JSONConstants.TAG_FEEDBACK_NAME;
import static com.botree.retailerssfa.service.JSONConstants.TAG_FEEDBACK_TYPE;

/**
 * Created by shantarao on 8/11/16.
 */

public class FeedBackMasterModel {

    @SerializedName(TAG_FEEDBACK_NAME)
    private String feedbackName;

    @SerializedName(TAG_FEEDBACK_TYPE)
    private String feedbackType;

    private String modDt;

    private String cmpCode;

    private String consoleFlag;

    private String uploadFlag;

    private String changeNo;

    public String getFeedbackName() {
        return feedbackName;
    }

    public void setFeedbackName(String feedbackName) {
        this.feedbackName = feedbackName;
    }

    public String getFeedbackType() {
        return feedbackType;
    }

    public void setFeedbackType(String feedbackType) {
        this.feedbackType = feedbackType;
    }

    public String getModDt() {
        return modDt;
    }

    public void setModDt(String modDt) {
        this.modDt = modDt;
    }

    public String getCmpCode() {
        return cmpCode;
    }

    public void setCmpCode(String cmpCode) {
        this.cmpCode = cmpCode;
    }

    public String getConsoleFlag() {
        return consoleFlag;
    }

    public void setConsoleFlag(String consoleFlag) {
        this.consoleFlag = consoleFlag;
    }

    public String getUploadFlag() {
        return uploadFlag;
    }

    public void setUploadFlag(String uploadFlag) {
        this.uploadFlag = uploadFlag;
    }

    public String getChangeNo() {
        return changeNo;
    }

    public void setChangeNo(String changeNo) {
        this.changeNo = changeNo;
    }
}
